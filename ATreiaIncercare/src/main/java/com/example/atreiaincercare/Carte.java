package com.example.atreiaincercare;

public class Carte {
    private String titlu;
    private String autor;
    private String imagineSrc;


    public String getTitlu() {
        return titlu;
    }

    public void setTitlu(String titlu) {
        this.titlu = titlu;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getImagineSrc() {
        return imagineSrc;
    }

    public void setImagineSrc(String imagineSrc) {
        this.imagineSrc = imagineSrc;
    }

}
